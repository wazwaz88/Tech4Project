package operator.shortHandAssignmentOperators;

public class ShortHandAssignment {
    public static void main(String[] args) {

        int age = 5;

        System.out.println(age);

        age += 3; // age = age + 3/ short-hand assignment



        System.out.println(age);

        age -= 2;

        System.out.println(age);

        age *= 2;
        System.out.println(age);


        age /= 3;
        System.out.println(age);


        System.out.println();
        int a = 8;
        int b = 3;

        a += b;
        System.out.println(a);
        a -= b;
        System.out.println(a);
        a *= b;
        System.out.println(a);
        a /= b;
        System.out.println(a);
        a %= b;
        System.out.println(a);




    }
}
