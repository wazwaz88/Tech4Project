package operator.arithmeticOperators;

public class ArithmeticOperators {
    public static void main(String[] args){

        int number1 = 9;
        int number2 = 3;

        // addition
        double sum = number1 + number2; // 8

        // subtraction
        double sub = number1 - number2; // 2

        // multiplication
        double times = number1 * number2; // 15

        // division
        double div = number1 / number2;

        // remainder
        double remainder = number1 % number2; // 2= 5-3*1 :)

        System.out.println("Addition is = " + sum);
        System.out.println("Subtraction is = " + sub);
        System.out.println("Multiplication is = " + times);
        System.out.println("Division is = " + div);
        System.out.println("Remainder is = " + remainder);






    }
}
