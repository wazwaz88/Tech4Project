package operator.arithmeticOperators;

public class Exercise1 {
    public static void main(String[] args){


        int num1 = 21;
        int num2 = 4;

        int sum = num1 + num2;
        int sub = num1 - num2;
        int times = num1 * num2;
        int div = num1 / num2;
        int remainder= num1 % num2;


        System.out.println("Addition of num1/num2 = " + sum);
        System.out.println("Subtraction of num1/num2 = " + sub);
        System.out.println("Multiplication of num1/num2 = " + times);
        System.out.println("Division of num1/num2 = " + div);
        System.out.println("Remainder of num1/num2 = " + remainder);

    }
}
