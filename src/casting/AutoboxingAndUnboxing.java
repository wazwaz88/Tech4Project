package casting;

public class AutoboxingAndUnboxing {
    public static void main(String[] args) {


        //Autoboxing- primitive to a object. (Wrapper Class)

        float num = 45.6F;
        Float numFloat = num;

        //Unboxing- object to primitive

        Character character = 'D';
        char characterPrimitive = character;






    }
}
