package primitives;

public class LearningFloat {
    public static void main(String[] args){
        //float can handle decimal numbers. used for money. Have to add an F at the end
        //same as int for space but provides decimal

        float myFirstFloat = 14.5F;
        float mySecondFloat = 45.2F;

        float myThirdFloat = 56.2345677F; //gets rounded up. 6 decimal points only.

        System.out.println(myFirstFloat);
        System.out.println(mySecondFloat);
        System.out.println(myThirdFloat);

    }
}
